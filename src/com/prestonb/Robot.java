package com.prestonb;

public class Robot {

	String name;
	boolean enabled;
	int score;
	int feetPerSec;
	
	public Robot(String name, boolean enabled, int score, int feetPerSec) {
		super();
		this.name = name;
		this.enabled = enabled;
		this.score = score;
		this.feetPerSec = feetPerSec;
	}
	
}
